    /*
     * Draw geometry
     *
     * @var type
     * @var arrPoint = []
     * @var simpleMarkerSymbol = {}
     * @var attributesPoint = {}
     */
    function drawGeometry(
       type,
       arrPoint,
       simpleMarkerSymbol,
       attributesPoint = {}
    ) {
       let geometry = {
          type: type,
       };
 
       switch (type) {
          case "point":
             geometry = {
                ...geometry,
                longitude: arrPoint[0],
                latitude: arrPoint[1],
             };
             break;
 
          case "polyline":
             geometry = {
                ...geometry,
                paths: arrPoint
             };
             break;
 
          case "polygon":
             geometry = {
                ...geometry,
                rings: arrPoint
             };
             break;
       }
 
       const popupTemplatePoint = {
          title: "{name}",
          content: "{content}",
       };
 
       var arrParams = [
          geometry,
          simpleMarkerSymbol,
          attributesPoint,
          popupTemplatePoint,
       ];
       handleAddGraphicLayer(arrParams);
    }
 
    /*
     * Handle add graphic layer
     *
     * @var arrParams = []
     */
    function handleAddGraphicLayer(arrParams) {
       const pointGraphic = new Graphic({
          geometry: arrParams[0],
          symbol: arrParams[1],
 
          attributes: arrParams[2],
          popupTemplate: arrParams[3],
       });
       graphicsLayer.add(pointGraphic);
    }
 
